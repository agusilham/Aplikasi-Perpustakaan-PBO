            private void LoadData(){
                Object kolom[] = {"NIS","N. SISWA","TTL","J.K","KELAS","ALAMAT","AGAMA","TGL DAFTAR","BERLAKU H"};
                Tabgota = new DefaultTableModel(null, kolom);
                Tabta.setModel(Tabgota);
                jScrollPane1.getViewport().add(Tabta,null);
                try{
                    Con = null;
                    Class.forName("com.mysql.jdbc.Driver");
                    Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/perpustakaan","root","");
                    String sql = "" + "SELECT * from anggota";
                    Statement stat = con.createStatement();
                    ResultSet res = stat.executeQuery(sql);
                    while(res.next()){
                        String NIS = res.getString("NIS");
                        String NamaSiswa = res.getString("Nama");
                        String TTL = res.getString("TempatTglLahir");
                        String JenisKelamin = res.getString("JenisKelamin");
                        String Kelas = res.getString("Kelas");
                        String Alamat = res.getString("Alamat");
                        String Agama = res.getString("Agama");
                        String TglDaftar = res.getString("TglPendaftaran");
                        String Berlaku = res.getString("BerlakuHingga");
                        Object[] data = {NIS, NamaSiswa, TTL, JenisKelamin, Kelas, Alamat, Agama, TglDaftar, Berlaku, };
                        Tabgota.addRow(data);
                    }
                    Tabta.getColumnModel().getColumn(0).setPreferredWidth(50);
                    Tabta.getColumnModel().getColumn(1).setPreferredWidth(80);
                    Tabta.getColumnModel().getColumn(2).setPreferredWidth(80);
                    Tabta.getColumnModel().getColumn(3).setPreferredWidth(20);
                    Tabta.getColumnModel().getColumn(4).setPreferredWidth(40);
                    Tabta.getColumnModel().getColumn(5).setPreferredWidth(80);
                    Tabta.getColumnModel().getColumn(6).setPreferredWidth(50);
                    Tabta.getColumnModel().getColumn(7).setPreferredWidth(80);
                    Tabta.getColumnModel().getColumn(8).setPreferredWidth(80);
                } catch (Exception ex){
                    JOptionPane.showMessageDialog(this,"Error: " + ex);
                }
            }
