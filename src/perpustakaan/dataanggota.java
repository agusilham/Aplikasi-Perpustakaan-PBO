    private void NISActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_NISActionPerformed
        String x=NIS.getText();
        try{
            Class.forName("com.mysql.jdbc.Driver");
            Connection connection=DriverManager.getConnection("jdbc:mysql://localhost:3306/perpustakaan","root","");
            Statement stat=connection.createStatement();
            String cari = "SELECT * FROM anggota WHERE NIS='"+x+"'";
            ResultSet rst=stat.executeQuery(cari);
            if(rst.next()){
                JOptionPane.showMessageDialog(null,"Data ditemukan");
                LoadData();
                System.out.print(rst.getString(1));
                NIS.setText(rst.getString(1));
                Nama.setText(rst.getString(2));
                TTL.setText(rst.getString(3));
                if (rst.getString(4).equals("Laki-Laki")){
                    RBLaki.setSelected(true);
                    }else{
                    RBPerempuan.setSelected(true);
                    }
                Alamat.setText(rst.getString(5));
                Agama.setText(rst.getString(6));
                TD.setText(rst.getString(7));
                Behi.setText(rst.getString(8));
                CBKelas.setSelectedItem(rst.getString(9));
        }else{
                JOptionPane.showMessageDialog(null,"Data tidak ada");
            }
        }catch(Exception e){
            System.out.print(e);
            JOptionPane.showMessageDialog(null, "Koneksi Gagal");
        }
    }//GEN-LAST:event_NISActionPerformed

    private void editActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_editActionPerformed
        int confirm = JOptionPane.showConfirmDialog(null,"Yakin untuk mengubah data ini?","Confirmation",JOptionPane.YES_NO_OPTION);
        try{
            Class.forName("com.mysql.jdbc.Driver");
            Connection connection = DriverManager.getConnection("jdbc:mysql://localhost:3306/perpustakaan","root","");
            String sql="update anggota set nis=?,nama=?,tempattgllahir=?,jeniskelamin=?,alamat=?,agama=?,tglpendaftaran=?,berlakuhingga=?,kelas=? where nis='"+NIS.getText()+"'";
            PreparedStatement st=connection.prepareStatement(sql);
            if(confirm==0){
                try{
                    st.setString(1,NIS.getText());
                    st.setString(2,Nama.getText());
                    st.setString(3,TTL.getText());
                    if (RBLaki.isSelected()){
                    st.setString(4,RBLaki.getText());
                    }if(RBPerempuan.isSelected()){
                    st.setString(4,RBPerempuan.getText());
                    }
                    st.setString(5,Alamat.getText());
                    st.setString(6,Agama.getText());
                    st.setString(7,TD.getText());
                    st.setString(8,Behi.getText());
                    String skelas;
                    int kelas = CBKelas.getSelectedIndex();
                    if(kelas==0){st.setString(9,skelas="X");}
                    if(kelas==1){st.setString(9,skelas="XI");}
                    if(kelas==2){st.setString(9,skelas="XII");}
                    st.executeUpdate();
                    JOptionPane.showMessageDialog(null, "Update Data Sukses");
                    LoadData();
                }catch (Exception e){
                    JOptionPane.showMessageDialog(null, "Update Data Gagal");
                }
            }
        }catch (Exception e){
            JOptionPane.showMessageDialog(null, "Tidak ada Data");
        }
    }//GEN-LAST:event_editActionPerformed

