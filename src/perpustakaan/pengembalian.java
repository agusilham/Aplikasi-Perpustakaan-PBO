    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String x = JOptionPane.showInputDialog(null,"Masukkan Kode Pinjam");
        try{
            Class.forName("com.mysql.jdbc.Driver");
            Connection connection=DriverManager.getConnection("jdbc:mysql://localhost:3306/perpustakaan","root","");
            Statement stat=connection.createStatement();
            String cari = "SELECT * FROM datakembali WHERE kodepinjam='"+x+"'";
            ResultSet rst=stat.executeQuery(cari);
            if(rst.next()){
                JOptionPane.showMessageDialog(null,"Data ditemukan");
                LoadData();
                System.out.print(rst.getString(1));
                kodepinjam.setText(rst.getString(1));
                nis.setText(rst.getString(2));
                Nasis.setText(rst.getString(3));
                CBKelas.setSelectedItem(rst.getString(4));
                KB.setText(rst.getString(5));
                Nabuk.setText(rst.getString(6));
                Pen.setText(rst.getString(7));
                TglPin.setText(rst.getString(8));
                TglKem.setText(rst.getString(9));
                TglPem.setText(rst.getString(10));
                if (rst.getString(11).equals("Tepat Waktu")){
                    btntepatwaktu.setSelected(true);
                    dendatxt.setEditable(false);
                    hilangtxt.setEditable(false);
                    }if(rst.getString(11).equals("Terlambat")){
                    btnterlambat.setSelected(true);
                    dendatxt.setText(rst.getString(12));
                    hilangtxt.setEditable(false);
                    }if(rst.getString(11).equals("Hilang")){
                    btnhilang.setSelected(true);
                    hilangtxt.setText(rst.getString(12));
                    dendatxt.setEditable(false);
                }
            }else{
                JOptionPane.showMessageDialog(null,"Data tidak ada");
            }
        }catch (Exception e){
            System.out.print(e);
            JOptionPane.showMessageDialog(null,"Koneksi Gagal");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

