    private void btnterlambatActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnterlambatActionPerformed
        dendatxt.setEditable(true);
        hilangtxt.setEditable(false);
        hilangtxt.setText("0");
        DateFormat df = new SimpleDateFormat("yyyy-MM-dd");
        try{
            java.util.Date TanggalKem = df.parse(TglKem.getText());
            java.util.Date TanggalPem = df.parse(TglPem.getText());
            
            long Hari1 = TanggalKem.getTime();
            long Hari2 = TanggalPem.getTime();
            long diff = Hari2 - Hari1;
            long lama = diff / (24*60*60*1000);
            long denda = lama * 1000;
            dendatxt.setText(Long.toString(denda));
        }catch(ParseException e){
            e.printStackTrace();
        }
        
        
    }//GEN-LAST:event_btnterlambatActionPerformed

