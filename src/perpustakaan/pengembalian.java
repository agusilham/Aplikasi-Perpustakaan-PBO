    private void saveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveActionPerformed
        String x=kodepinjam.getText();
        try{
            Class.forName("com.mysql.jdbc.Driver");
            Connection connection=DriverManager.getConnection("jdbc:mysql://localhost:3306/perpustakaan","root","");
            Statement stat=connection.createStatement();
            String sql2="delete from datapinjam where kodepinjam='"+x+"'";
            stat.executeUpdate(sql2);
            LoadData();
            int kelas = CBKelas.getSelectedIndex();
            String skelas = "";
            if(kelas==0){skelas="X";}
            if(kelas==1){skelas="XI";}
            if(kelas==2){skelas="XII";}
            String btnStatus="";
            String nilaidenda="0";
            if (btntepatwaktu.isSelected()){
                    btnStatus = btntepatwaktu.getText();
                    dendatxt.setText("0");
                }if(btnhilang.isSelected()){
                    btnStatus = btnhilang.getText();
                    nilaidenda= hilangtxt.getText();
                }if(btnterlambat.isSelected()){
                    btnStatus = btnterlambat.getText();
                    nilaidenda= dendatxt.getText();
                }
            String sqli=("insert into datakembali values('"+kodepinjam.getText()+"','"+nis.getText()+"','"+Nasis.getText()+"','"+skelas+"','"+KB.getText()+"','"+Nabuk.getText()+"','"+Pen.getText()+"','"+TglPin.getText()+"','"+TglKem.getText()+"','"+TglPem.getText()+"','"+btnStatus+"','"+nilaidenda+"')");
            stat.executeUpdate(sqli);
            JOptionPane.showMessageDialog(null,"Data Tersimpan");
            LoadData();
        }catch(Exception e){
            System.out.print(e);
            JOptionPane.showMessageDialog(null,"Koneksi Gagal");
        }
    }//GEN-LAST:event_saveActionPerformed

